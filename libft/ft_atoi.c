#include "libft.h"

int	ft_atoi(const char *str)
{
	int	nb;
	int	i;

	while ((*str == ' ') || (*str >= 9 && *str <= 13))
		str++;
	i = 0;
	while ((*str == '-' || *str == '+')
		&& (*(str + 1) != '-' && *(str + 1) != '+'))
	{
		if (*str == '-')
			i += 1;
		str++;
	}
	nb = 0;
	while (*str >= '0' && *str <= '9')
	{
		nb *= 10;
		nb += *str - '0';
		str++;
	}
	if (i % 2 == 1)
		nb *= -1;
	return (nb);
}
